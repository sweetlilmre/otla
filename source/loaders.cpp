void make_table (char *t ,int t00, int t01, int t10 , int t11, int max)
{
    int i=0;
    do
        if (i<t00)
            t[i]=0xfc;
        else
            if (i<t01)
                t[i]=0xfd;
            else
                if (i<t10)
                    t[i]=0xfe;
                else
                    if (i<t11)
                        t[i]=0xff;
                    else
                        t[i]=0x40;
    while (++i<max);

}

                               
